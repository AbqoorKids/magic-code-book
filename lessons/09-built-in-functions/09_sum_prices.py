# دمج أكثر من تابع جاهز

# القائمة تحتوي على أسماء الألعاب
toys = ["روبلكس", "كرسي ألعاب", "لابوبو خشبي"]

# القائمة تحتوي على أسعار الألعاب
prices = [50, 150, 80]

# مجموع الأسعار
sum_prices = sum(prices)

# عدد العناصر
print("عدد العناصر:", len(toys))

# متوسط السعر مع التقريب
print("متوسط السعر:", round(sum_prices / len(prices)))
