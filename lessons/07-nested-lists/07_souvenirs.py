# أصدقاء عبقور والتذكارات الخاصة بكل واحد منهم

souvenirs = [
    ["كركور", ["حذاء", "حقيبة"]],
    ["فرفور", ["نظارات", "وشاح"]],
    ["قرقور", ["قبعة", "قميص"]]
]

for row in souvenirs:
    # طباعة اسم الصديق
    print("صديق:", row[0])
    # المرور على كل تذكار
    for souvenir in row[1]:
        print("  تذكار:", souvenir)
