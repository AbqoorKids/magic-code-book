# مثال 1: هل العدد زوجي أم فردي؟
number = 10
if number % 2 == 0:
    print("العدد", number, "هو عدد زوجي.")
if number % 2 != 0:
    print("العدد", number, "هو عدد فردي.")

print("-----")

# مثال 2: تغيير قيمة المتغير
apples = 4
apples = apples + 2
print("عدد التفاحات الآن:", apples)

# مثال شرط إضافي للتوضيح
# هنا نتحقق إن apples مقسومًا على 2 يساوي 3
if apples / 2 == 3:
    print("نعم، apples/2 == 3 صحيح.")
